package MovieTicketModel;

/**
 *
 * @author sourabh_deshkulkarni
 */
public class MovieInfo {
    
    private String movieName;
    private String availableTickets;

    /**
     * @return the movieName
     */
    public String getMovieName() {
        return movieName;
    }

    /**
     * @param movieName the movieName to set
     */
    public void setMovieName(String movieName) {
        this.movieName = movieName;
    }

    /**
     * @return the availableTickets
     */
    public String getAvailableTickets() {
        return availableTickets;
    }

    /**
     * @param availableTickets the availableTickets to set
     */
    public void setAvailableTickets(String availableTickets) {
        this.availableTickets = availableTickets;
    }
    
    
    
    
    
    
}
